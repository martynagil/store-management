package com.github.martynagil.storemanagement;

public class Product {

    private String name;
    private String brand;
    private String type;
    private double price;

    public Product(String name, String brand, String type, double price) {
        this.name = name;
        this.brand = brand;
        this.type = type;
        this.price = price;
    }
}
